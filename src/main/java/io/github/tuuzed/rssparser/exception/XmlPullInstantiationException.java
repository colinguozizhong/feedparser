package io.github.tuuzed.rssparser.exception;

public class XmlPullInstantiationException extends RuntimeException {

    public XmlPullInstantiationException() {
        super();
    }


    public XmlPullInstantiationException(String s) {
        super(s);
    }

    public XmlPullInstantiationException(String message, Throwable cause) {
        super(message, cause);
    }

    public XmlPullInstantiationException(Throwable cause) {
        super(cause);
    }

}
